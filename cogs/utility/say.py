class saycmd(commands.Cog):
    def __init__(self, bot):
        self.bot = bot

    @commands.command(aliases=['announce'])
    async def say(self, ctx, channel: typing.Union[discord.TextChannel, str] = None, *, message: str=""):
        '''
        say

        Say something as the bot
        '''
        await ctx.message.delete()
        # deleteing original message

        if channel is None:
            return await ctx.send("No message was specifyed.")

        if isinstance(channel, discord.TextChannel) and message == "":
            return await ctx.send("No message was specifyed")

        if isinstance(channel, discord.TextChannel):
            return await channel.send(message)
        else:
            return await ctx.send(channel + " " + message)





